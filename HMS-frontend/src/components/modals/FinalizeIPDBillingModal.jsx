function fmtTime(iso) {
  if (!iso) return ''
  return new Date(iso).toLocaleString('en-IN', {
    timeZone: 'Asia/Kolkata', day: '2-digit', month: 'short',
    hour: '2-digit', minute: '2-digit', hour12: true,
  })
}

// CUSTOM items in the IPD invoice come from two distinct sources:
//   1. Auto-calculated charges built fresh each time the modal opens — food,
//      patient services (daily/one-time), ambulance bookings.
//   2. User-entered free-text items (e.g. "Extra bedsheets", "Maintenance fee").
//
// On reload we must drop (1) so it can be regenerated with current quantities,
// but preserve (2) verbatim. The bill stores no metadata distinguishing them,
// so we infer from the description shape.
//
// Auto patterns (must match exactly — see the auto-build loop further down):
//   • "Ambulance"            — bare type, no vehicle
//   • "Ambulance — …"        — type/vehicle composed via " — " separator
//   • "<service.name>"       — one-time charge (registration, custom one-time)
//   • "<service.name> (N meal)" / "(N meals)" — FOOD service
//   • "<service.name> (N day)"  / "(N days)"  — daily-rate service
//
// Anything else is user-entered and survives the reload.
function isAutoGeneratedCustomDescription(description, patientServicesList) {
  if (!description) return false
  if (description === 'Ambulance' || description.startsWith('Ambulance — ')) return true
  if (!Array.isArray(patientServicesList)) return false
  for (const svc of patientServicesList) {
    if (!svc?.isActive || !svc.name) continue
    const name = svc.name
    if (description === name) return true
    // " (N meal)", " (N meals)", " (N day)", " (N days)" suffix
    if (description.startsWith(name + ' (')) {
      const tail = description.slice(name.length + 2, -1)  // strip "<name> (" and trailing ")"
      if (/^\d+ (meal|meals|day|days)$/.test(tail)) return true
    }
  }
  return false
}

function countMealSlots(admitDate, dischargeDate, chargeTime) {
  if (!chargeTime) return 0
  const [h, m] = chargeTime.split(':').map(Number)
  const admit = new Date(admitDate)
  const discharge = new Date(dischargeDate)
  let count = 0
  const day = new Date(admit)
  day.setHours(0, 0, 0, 0)
  while (day <= discharge) {
    const slot = new Date(day)
    slot.setHours(h, m, 0, 0)
    if (slot >= admit && slot <= discharge) count++
    day.setDate(day.getDate() + 1)
  }
  return count
}

